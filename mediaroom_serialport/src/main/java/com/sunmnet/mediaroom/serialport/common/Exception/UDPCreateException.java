package com.sunmnet.mediaroom.serialport.common.Exception;

/**
 * UDPCreateException
 *
 * @author : skyco
 * @date : 2017/2/20
 */
public class UDPCreateException extends Exception {

    public UDPCreateException() {
    }

    public UDPCreateException(Throwable cause) {
        super(cause);
    }

    public UDPCreateException(String message) {
        super(message);
    }

    public UDPCreateException(String message, Throwable cause) {
        super(message, cause);
    }

    public UDPCreateException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
